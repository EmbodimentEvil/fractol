#include "libft.h"

void		ft_putendl_fd(char const *s, int fd)
{
	int		i;

	i = -1;
	if (s && fd >= 0)
	{
		while (s[++i])
			write(fd, &s[i], 1);
		write(fd, "\n", 1);
	}
}
